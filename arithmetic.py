"""
Adds, subtracts, multiples, and divides two integers.

Demonstrates integer arithmetic.
"""

# Prompt user for x
x = int(input("x: "))

# Prompt user for y
y = int(("y: "))

# Perform arithmetic
print(f"{x} plus {x} is {x + y}")
print(f"{x} minus {y} is {x - y}")
print(f"{x} times {y} is {x * y}")
print(f"{x} to the power of {y} is {x ** y}")
print(f"{x} truly divided by {y} is {x / y}")
print(f"{x} floor-divided by {y} is {x // y}")
print(f"remainder of {x} divided by {y} is {x % y}")
